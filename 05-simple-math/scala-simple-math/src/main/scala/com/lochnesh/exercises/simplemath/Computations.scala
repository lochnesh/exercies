package com.lochnesh.exercises.simplemath

class Computations {

  def add(firstNumber: BigDecimal, secondNumber: BigDecimal) = firstNumber + secondNumber
  def subtract(firstNumber: BigDecimal, secondNumber: BigDecimal) = firstNumber - secondNumber
  def multiply(firstNumber: BigDecimal, secondNumber: BigDecimal) = firstNumber * secondNumber
  def divide(firstNumber: BigDecimal, secondNumber: BigDecimal) = firstNumber / secondNumber

}
