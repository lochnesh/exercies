package io.github.lochnesh

class Computations {

  def add(firstNumber: Int, secondNumber: Int) = firstNumber + secondNumber
  def subtract(firstNumber: Int, secondNumber: Int) = firstNumber - secondNumber
  def multiply(firstNumber: Int, secondNumber: Int) = firstNumber * secondNumber
  def divide(firstNumber: Int, secondNumber: Int) = firstNumber / secondNumber

}
